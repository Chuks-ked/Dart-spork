//variable

void main() {
  //strings
  // var name = 'John';
  String name = 'John';
  print(name);

  //Integer
  // var x = 41;
  int y = 41;
  print(y);

  //Dynamic
  dynamic firstName = 'Tim';
  print(firstName);

  //Const and Final
  const String fullName = 'John Elder';
  print(fullName);

  final String nickName = "J";
  print(nickName);

  //declaring and not using it
  var myName;
  print(myName);

  myName = "John";
  print(myName);
}

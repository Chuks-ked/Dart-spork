void main() {
  //Data Types
  //Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // Strings
  String firstName = "John";
  print("String: $firstName");

  //Integers
  int myNum = 41;
  print("Int: $myNum");

  //Doubles
  double otherNum = 19.9;
  print("Double: $otherNum");

  //Booleans
  bool myBool = false;
  print("Bool: $myBool");

  //Dynamic
  dynamic fullName = "Joe Dow";
  print("Dynamic: $fullName");
}
